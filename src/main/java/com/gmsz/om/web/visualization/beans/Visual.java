package com.gmsz.om.web.visualization.beans;


//设备状态
public class Visual {
	
	private int stop;
	private int run;
	private int rerun;
	public int getStop() {
		return stop;
	}
	public void setStop(int stop) {
		this.stop = stop;
	}
	public int getRun() {
		return run;
	}
	public void setRun(int run) {
		this.run = run;
	}
	public int getRerun() {
		return rerun;
	}
	public void setRerun(int rerun) {
		this.rerun = rerun;
	}
	
	
	
}
